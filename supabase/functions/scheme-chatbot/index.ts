import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const systemPrompt = `You are a helpful assistant for the Rural Services Portal â€” an Indian government scheme aggregator. Your job is to help citizens find relevant government schemes based on their category (Farmer, Student, Woman, General), state, and needs.

Key scheme categories:
- Farmers: PM Kisan, PM Fasal Bima Yojana, Kisan Credit Card, Soil Health Card, etc.
- Students: National Scholarship Portal, PM Vidyalakshmi, Inspire Scholarship, etc.
- Women: Sukanya Samriddhi, Beti Bachao Beti Padhao, Stand Up India, Ujjwala Yojana, etc.
- General: Jan Dhan Yojana, Ayushman Bharat, Atal Pension, Digital India, Skill India, Startup India, etc.

Guidelines:
- Ask clarifying questions to narrow down the right schemes.
- Provide scheme names, brief descriptions, and suggest visiting the portal for full details.
- Be concise and helpful. Answer in the language the user writes in (English or Hindi).
- If unsure, recommend checking the Search page with appropriate filters.
- Do NOT make up scheme details that you don't know.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again later." }), {
          status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Service temporarily unavailable." }), {
          status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("chatbot error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
